#include <stdio.h>

int main() {
    int umur = 15;          // Variabel bilangan bulat
    float tinggi = 165.5;   // Variabel bilangan desimal
    char huruf = 'A';       // Variabel karakter

    printf("Umur: %d tahun\n", umur);
    printf("Tinggi: %.1f cm\n", tinggi);
    printf("Huruf favorit: %c\n", huruf);

    return 0;
}
