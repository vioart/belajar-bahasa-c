#include <stdio.h>

int main() {
    int a = 10, b = 3;
    printf("Penjumlahan: %d\n", a + b);   // Hasil: 13
    printf("Pengurangan: %d\n", a - b);   // Hasil: 7
    printf("Perkalian: %d\n", a * b);     // Hasil: 30
    printf("Pembagian: %d\n", a / b);     // Hasil: 3 (pembagian bilangan bulat)
    printf("Modulus: %d\n", a % b);       // Hasil: 1 (sisa bagi)
    return 0;
}
